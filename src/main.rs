/// ### 解构式赋值
/// 该功能于 Rust 1.59 版本引入：你可以在赋值语句的左式中使用元组、切片或结构体进行匹配赋值。
/// 9. 🌟🌟
/// > Note: 解构式赋值只能在 Rust 1.59 或者更高版本中使用
/// ```rust,editable
/// fn main() {
///     let (x, y);
///     (x,..) = (3, 4);
///     [.., y] = [1, 2];
///     // 填空，让代码工作
///     assert_eq!([x,y], __);
/// } 
/// 
/// 结论： 1. 使用解构式赋值来将元组 (3, 4) 中的第一个元素赋值给变量 x，并将切片 [1, 2] 中的最后一个元素赋值给变量 y。
/// 结论:  元组()可以包含不同的数据类型,[]只能包含同一类型
fn practice9() {
    let (x, y);
    (x,..) = (3, 4);
    [.., y] = [1, 2];
    // 填空，让代码工作
    assert_eq!([x,y], [3,2]);

    let (x1,x2,x3);
    (x1, x2, x3) = (5, 6.0, "dawdaw"); //支持不同的类型
    // [x1,x2,x3] = [5, 6.0, "dawdaw"]; //不支持不同的类型
}

fn main()
{
    practice9();
}

