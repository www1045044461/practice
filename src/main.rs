/// 若后面的变量声明的名称和之前的变量相同，则我们说：第一个变量被第二个同名变量遮蔽了( shadowing )
fn practice5() {
    let x: i32 = 5;
    {
        let x = 12;
        assert_eq!(x, 12); 
        // assert_eq!(x, 5); 
        //运行到此时是错误的，因为在这个作用域内，x 被重新声明并赋值为 12，所以 x 的值是 12，而不是 5。
    }
    assert_eq!(x, 5);
    // assert_eq!(x, 12);
    //运行到此时也是错误的，因为在外部作用域内，x 的值仍然是 5，而不是 12。

    let x = 42;
    println!("{}", x); // 输出 "42"
}
