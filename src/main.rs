/// 8. 🌟🌟 我们可以将 `let` 跟一个模式一起使用来解构一个元组，最终将它解构为多个独立的变量
/// 
/// > 提示: 可以使用变量遮蔽或可变性
/// 
/// ```rust,editable
/// // 修复下面代码的错误并尽可能少的修改
/// fn main() {
///     let (x, y) = (1, 2);
///     x += 2;
/// 
///     assert_eq!(x, 3);
///     assert_eq!(y, 2);
/// }
/// ```
/// 结论: 1. 使用可变性来修改变量 x 的值。
fn practice8_1() {
    let (mut x,y) = (1, 2);
    x += 2;
    assert_eq!(x, 3);
    assert_eq!(y, 2);
}

/// 结论: 2. 使用变量遮蔽来重新绑定变量 x 的值。
fn practice8_2() {
    let (x, y) = (1, 2);
    let x = 3;
    assert_eq!(x, 3);
    assert_eq!(y, 2);
}

